    @commands.command(aliases=['swap'])
    async def swap_dj(self, ctx: commands.Context, *, member: discord.Member = None):
        """다른 유저한테 DJ를 넘겨줍니다!"""
        player: Player = self.bot.wavelink.get_player(guild_id=ctx.guild.id, cls=Player, context=ctx)

        if not player.is_connected:
            return

        if not self.is_privileged(ctx):
            return await ctx.send('어드민 혹은 DJ만 이 명령어를 사용할수 있습니다!', delete_after=15)

        members = self.bot.get_channel(int(player.channel_id)).members

        if member and member not in members:
            return await ctx.send(f'{member}님은 이 보이스 채널에 없습니다!', delete_after=15)

        if member and member == player.dj:
            return await ctx.send('현재 DJ한테 DJ를 넘겨줄수 없습니다! :)', delete_after=15)

        if len(members) <= 2:
            return await ctx.send('DJ권한을 넘겨줄 유저가 없습니다', delete_after=15)

        if member:
            player.dj = member
            return await ctx.send(f'{member.mention}님! 당신은 이제 DJ입니다!')

        for m in members:
            if m == player.dj or m.bot:
                continue
            else:
                player.dj = m
                return await ctx.send(f'{member.mention}님! 당신은 이제 DJ입니다!')


